#include <iostream>


int main()
{
    // 1 Byte = 8B bit
    bool myValue0 = false; //true

    // 1 Byte = 8 bit
    char myValue1 = 10;

    // 2 Byte = 16 bit
    short myValue2 = 42;

    // 4 Byte = 32 bit
    int myValue3 = 22;

    // 4 Byte = 32 bit
    float myValue4 = 12.0f;

    // 8 Byte = 64 bit
    double myValue5 = 13.0;


    return 0;
}
